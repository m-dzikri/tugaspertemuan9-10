package com.pbo.no2polymorphism;

/**
 *
 * @author HP
 */
public class MyDate {
    private int year;
    private int month;
    private int day;

    public MyDate(int year, int month, int day) {
        this.year = year;
        this.month = month;
        this.day = day;
    }

    @Override
    public String toString() {
        return String.format("%d/%d/%d", day, month, year);
    }
}
